            }

            while (IsConnected)
            {
                try
                {
                    NetworkStream networkStream = ClientSocket.GetStream();
                    byte[] bytes = new byte[1024];
                    networkStream.Read(bytes, 0, bytes.Length);
                    string data = System.Text.Encoding.ASCII.GetString(bytes);
                    if (data == noise)
                    {
                        StopTCPClient();
                    }
                    Console.WriteLine(data);
                    IsReceiving = true;
