        public void StartTCPListener()
        {
            ServerSocket = new TcpListener(IPAddress.Parse("127.0.0.1"),Port);
            ServerSocket.Stop();
            ServerSocket.ExclusiveAddressUse = false;
            ClientSocket = default(TcpClient);
            try
            {
                ServerSocket.Start();
                ClientSocket = ServerSocket.AcceptTcpClient();
            }
            catch (Exception e)
            {
                ServerSocket.Stop();
                Console.WriteLine(e.ToString());
            }

  
            IsListening = true;
            //Socket Started
            while (IsListening)
            {
                try
                {
                    NetworkStream networkStream = ClientSocket.GetStream();
                    byte[] bytes = new byte[1024];
                    networkStream.Read(bytes, 0, bytes.Length);
                    string data = System.Text.Encoding.ASCII.GetString(bytes);
                    if (data == noise)
                    {
                        StopTCPListener();
                    }
                    Console.WriteLine(data);
                }
                catch (Exception e)
                {
                    Console.WriteLine(e.ToString());
                }
            }
            try
            {
                ClientSocket.Close();
                ServerSocket.Stop();
            }
            catch (Exception e)
            {
                Console.WriteLine(e.ToString());
            }

        }

        public void StopTCPListener()
        {
            IsListening = false;
            try
            {
                ServerSocket.Stop();
                var context = GlobalHost.ConnectionManager.GetHubContext<EyeTrackingModuleHub>();
                context.Clients.All.receiveConnectionStatus(Id, IsListening);
            }
            catch (Exception e)
            {
                Console.WriteLine(e.ToString());
